def evaluate(board: Board) -> int:
    """Return score in centipawns from White's perspective."""
    score = 0
    for r in range(ROWS):
        for c in range(COLS):
            piece = board.get_piece(r, c)
            if piece is None:
                continue
            val = PIECE_VALUES.get(piece.ptype, 0)
            pst_table = PST.get(piece.ptype)
            if pst_table:
                if piece.colour == Colour.WHITE:
                    val += pst_table[r][c]
                else:
                    val += pst_table[9 - r][9 - c]
            if piece.colour == Colour.WHITE:
                score += val
            else:
                score -= val
    return score